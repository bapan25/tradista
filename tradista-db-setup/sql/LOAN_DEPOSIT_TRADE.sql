CREATE table LOAN_DEPOSIT_TRADE(
LOAN_DEPOSIT_TRADE_ID BIGINT, 
FIXED_RATE DECIMAL(30,15), 
FLOATING_RATE_INDEX_ID BIGINT,
FLOATING_RATE_INDEX_TENOR VARCHAR(20),
DAY_COUNT_CONVENTION_ID BIGINT NOT NULL,
PAYMENT_FREQUENCY VARCHAR(20) NOT NULL,
END_DATE DATE NOT NULL,
FIXING_PERIOD VARCHAR(20),
SPREAD DECIMAL(30,15),
DIRECTION VARCHAR(7) NOT NULL,
MATURITY VARCHAR(20),
INTEREST_TYPE VARCHAR(8) NOT NULL,
COMPOUND_PERIOD VARCHAR(20),
INTEREST_PAYMENT VARCHAR(19) NOT NULL,
INTEREST_FIXING VARCHAR(19),
CONSTRAINT FK_LOAN_DEPOSIT_TRADE_ID FOREIGN KEY (LOAN_DEPOSIT_TRADE_ID) REFERENCES TRADE(ID),
CONSTRAINT FK_DAY_COUNT_CONVENTION_ID FOREIGN KEY (DAY_COUNT_CONVENTION_ID) REFERENCES DAY_COUNT_CONVENTION(ID),
CONSTRAINT FK_FLOATING_RATE_INDEX_ID FOREIGN KEY (FLOATING_RATE_INDEX_ID) REFERENCES INDEX(ID));