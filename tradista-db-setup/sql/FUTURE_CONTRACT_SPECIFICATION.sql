CREATE table FUTURE_CONTRACT_SPECIFICATION(
ID BIGINT PRIMARY KEY GENERATED ALWAYS AS IDENTITY  (START WITH 1, INCREMENT BY 1),
REFERENCE_RATE_INDEX_ID BIGINT,
REFERENCE_RATE_INDEX_TENOR VARCHAR(20) NOT NULL,
DAY_COUNT_CONVENTION_ID BIGINT,
NOTIONAL DECIMAL(30,15) NOT NULL,
CURRENCY_ID BIGINT,
NAME VARCHAR(50) UNIQUE NOT NULL,
EXCHANGE_ID BIGINT,
MATURITY_DATES_DATE_RULE_ID BIGINT,
CONSTRAINT FK_FUTURE_REFERENCE_RATE_INDEX_ID FOREIGN KEY (REFERENCE_RATE_INDEX_ID) REFERENCES INDEX(ID),
CONSTRAINT FK_FUTURE_MATURITY_DATES_DATE_RULE_ID FOREIGN KEY (MATURITY_DATES_DATE_RULE_ID) REFERENCES DATE_RULE(ID),
CONSTRAINT FK_FUTURE_CURRENCY_ID FOREIGN KEY (CURRENCY_ID) REFERENCES CURRENCY(ID),
CONSTRAINT FK_FUTURE_EXCHANGE_ID FOREIGN KEY (EXCHANGE_ID) REFERENCES EXCHANGE(ID),
CONSTRAINT FK_FUTURE_DAY_COUNT_CONVENTION_ID FOREIGN KEY (DAY_COUNT_CONVENTION_ID) REFERENCES DAY_COUNT_CONVENTION(ID));
